/**
 * エリア編集で、AIの全画面生成をそのまま採用するか（＝合成しない・継ぎ目なし）、囲った領域だけをベースへ
 * 合成するか（＝マスク外はベースのまま・範囲外を絶対に変えない）を決める純粋関数（260710→260711 再設計）。
 *
 * 【経緯 — 継ぎ目 / 湧き出し / 範囲外改変 のトレードオフの核心】
 * - 260707: 全画面生成をそのまま採用（合成なし）＝領域境界の継ぎ目が出ない、とクライアント確認。
 * - 260709: 「空いた場所に家具が湧く（spillover）」対策として、囲った領域だけを合成（マスク外はベースへ固定）に
 *   変更。湧き出しは止まったが境界に継ぎ目が再発。
 * - 260710: 継ぎ目再発の致命報告。テキストのみの編集を全画面直（合成なし）に戻して継ぎ目を消した。
 * - 260711: しかし「範囲内の椅子を消して」で“範囲外の別の椅子”が消える閉じ込め破れが発生（全画面直は範囲外を
 *   守らない）。→ **判定基準を「参照画像の有無」から「囲みの被覆率（幾何）」に作り替える**。囲まれた範囲は
 *   参照画像の有無を問わず必ず“範囲外を守る”。実質全画面（被覆が非常に大きい）だけ、守る外が無いので全画面直に任せる。
 *   さらに小さい囲み（局所）は runEdit 側で crop 経路に回し、そもそも範囲外の画素をモデルへ送らない（最も確実）。
 *
 * ※ 本関数は crop されなかった（＝全画面生成した）ときに「合成して範囲外を復元するか」を決める。
 *   crop 経路は別途 isConfinedRegion で分岐し、そちらは常に範囲だけを扱う。
 */

/** 囲みが実質「全画面」とみなす被覆率のしきい値。これ以上は守る“外”がほぼ無い＝全画面直（継ぎ目なし）に任せる。 */
export const GLOBAL_REGION_COVERAGE = 0.85;

export function shouldCompositeAreaEdit(params: {
  /** 囲った領域（placement）の総数。0 なら合成対象が無い。 */
  placementCount: number;
  /** 全画面出力をベース寸法へ合わせる方式。contain（レターボックス）は座標がズレるので合成しない。 */
  fitMode: 'cover' | 'contain';
  /** union 外接矩形の面積被覆率 0..1（囲みが画面のどれだけを占めるか）。 */
  unionCoverage: number;
  /** 全画面とみなす被覆率のしきい値（既定 GLOBAL_REGION_COVERAGE）。 */
  globalCoverageThreshold?: number;
}): boolean {
  if (params.fitMode !== 'cover') return false; // contain は座標がズレるので合成しない
  if (params.placementCount <= 0) return false; // 合成対象が無い
  const globalT = params.globalCoverageThreshold ?? GLOBAL_REGION_COVERAGE;
  // 実質全画面（囲みが画面のほぼ全部）＝守るべき外がほぼ無い → 全画面直で継ぎ目なし（260707 挙動を温存）。
  if (params.unionCoverage >= globalT) return false;
  // それ以外の“囲まれた範囲”は、参照画像の有無を問わず必ず合成して範囲外をベースへ厳密復元する（範囲外を絶対に変えない）。
  return true;
}
