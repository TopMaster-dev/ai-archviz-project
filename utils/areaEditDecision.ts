/**
 * エリア編集で、AIの全画面生成をそのまま採用するか（＝合成しない・継ぎ目なし）、囲った領域だけをベースへ
 * 合成するか（＝マスク外はベースのまま・湧き出し防止）を決める純粋関数（260710）。
 *
 * 【経緯 — 重要。ここが継ぎ目/湧き出しのトレードオフの核心】
 * - 260707: 全画面生成をそのまま採用（合成なし）＝領域境界の継ぎ目が出ない、とクライアント確認。
 * - 260709: 「指示していない空いた場所に家具が勝手に湧く（spillover）」対策として、囲った領域だけを合成
 *   （マスク外はベースへ固定）に変更。これにより湧き出しは止まったが、領域境界に継ぎ目（トーンの段差）が再発。
 * - 260710: クライアントから継ぎ目再発の致命報告。切り分けると、継ぎ目が出た事例はいずれも「参照画像なし＝
 *   テキストのみの編集（既存内容の変更：窓を昼に／照明を追加 等）」だった。テキストのみの変更は“新しい家具を
 *   置く”わけではないので spillover の心配がほぼ無い。→ テキストのみは全画面生成をそのまま採用（継ぎ目なし・
 *   260707の挙動に戻す）。参照画像あり（家具の追加/差し替え）のときだけ湧き出し防止で合成する。
 *
 * ※ 参照画像ありの配置（家具追加）の“位置ズレ＋継ぎ目”を両立で解消する本命は、AIに位置を委ねず
 *   プログラムで正確に配置してからAIでなじませる「確定配置＋なじませ」方式（別途提案）。本関数は
 *   その導入までの、テキスト編集の継ぎ目を止める確実な切り分け。
 */
export function shouldCompositeAreaEdit(params: {
  /** 参照画像を伴う配置があるか（家具の追加/差し替え）。true のときだけ湧き出し防止で合成する。 */
  hasReferenceImage: boolean;
  /** 囲った領域（placement）の総数。0 なら合成対象が無い。 */
  placementCount: number;
  /** 全画面出力をベース寸法へ合わせる方式。contain（レターボックス）は座標がズレるので合成しない。 */
  fitMode: 'cover' | 'contain';
}): boolean {
  if (params.fitMode !== 'cover') return false;
  if (params.placementCount <= 0) return false;
  // テキストのみ（参照画像なし）＝既存内容の変更 → 合成せず全画面のまま（継ぎ目なし）。
  // 参照画像あり＝家具の追加/差し替え → 合成（マスク外固定）で湧き出しを防ぐ。
  return params.hasReferenceImage;
}
