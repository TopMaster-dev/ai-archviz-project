/** スタイル/オブジェクトの有無からプロンプト用モードを自動決定（UIには出さない） */
export function resolvePromptMode(hasStyle: boolean, hasObjects: boolean): AiEditMode {
  if (hasObjects) return 'furniture_fixture';
  if (hasStyle) return 'lighting_atmosphere';
  return 'lighting_atmosphere';
}

/**
 * エリア編集1件の「意図」（260715 クライアント要望で複数種の編集を正しく出し分けるため）。
 * 1回の生成呼び出しは1オブジェクトなので、その1件のメモ文言＋参照画像の有無から意図を推定する。
 *  remove=削除 / material=素材・生地の表面適用（張り替え） / finish=壁床等の色・仕上げ変更 /
 *  lighting=時間帯・照明・環境 / replace=家具の差し替え / text=一般テキスト編集。
 */
export type AiEditIntent = 'remove' | 'material' | 'finish' | 'lighting' | 'replace' | 'text';

const RE_REMOVE =
  /(消(す|して|し去|去|えて)|削除|撤去|取り?除|除去|なくす|無くす|取り払|どかす|remove|delete|erase|get\s*rid|take\s*(it|them)?\s*(out|away)|clear\s*out)/i;
const RE_MATERIAL =
  /(素材|生地|テクスチャ|質感|張り?替|貼り?替|布地|ファブリック|material|texture|fabric|reupholster|upholster)/i;
const RE_FINISH =
  /(色|カラー|塗(る|って|装|り)|ペイント|壁紙|クロス|タイル|フローリング|床材|壁材|天井材|paint|colou?r|repaint|wallpaper|tile|flooring)/i;
const RE_LIGHTING =
  /(昼|夜|朝|夕|時間帯|明るく|暗く|照明|ライト|光量|空気感|ムード|天気|曇|晴|日差|夕焼|daytime|night|morning|evening|afternoon|dusk|dawn|sunset|sunrise|lighting|brighter|darker|mood|weather|sunny|cloudy|窓の外|外の景色|外環境)/i;
// 壁・床・天井などの「面の仕上げ（＝面に適用する建材/意匠）」。参照画像があっても家具の差し替え(replace)ではなく
// 面へのテクスチャ/仕上げ適用(material)として扱うために使う（③④・260717 クライアント要望: 面の形を保ち面全体に適用）。
// 【重要】日本語は語境界が無く部分一致で誤爆する。可動家具語の部分文字列になるトークンは入れない
//  （例: 畳→折り畳み(家具)、壁面→壁面収納(家具)、格子→格子戸(建具)、框→上がり框(建具)、造作→造作家具）。
//  面仕上げは、家具語と衝突しない具体語（緑化/タイル/塗装/羽目板/天井造作 等）だけで拾う（検証WFで誤爆を検出→修正）。
const RE_SURFACE =
  /(緑化|グリーンウォール|苔|芝生|芝|床面|天井面|腰壁|アクセントウォール|羽目板|ルーバー|ルーヴァー|格天井|天井造作|造作天井|折り上げ天井|折上げ天井|石張|石貼|石積|レンガ|煉瓦|タイル|モルタル|漆喰|珪藻土|塗装|塗り壁|塗り替|吹付|クロス|壁紙|フローリング|床材|壁材|天井材|サイディング|畳敷|琉球畳|置き畳|カーペット|絨毯|greenwall|moss|louver|paneling|panelling|plaster|stucco|render|tile|flooring|wallpaper|siding)/i;

function objIntentText(o: AiEditObjectReference): string {
  return [o.memo || '', ...(o.placementMemos || [])].join(' ');
}

/** 「削除（消す/remove）」の指示かどうか（テキストのみのエリア編集用の判定・純関数）。 */
export function isRemovalInstruction(text: string): boolean {
  return RE_REMOVE.test(text || '');
}

/** オブジェクト1件のエリア編集意図を推定する（純関数）。 */
export function detectAreaEditIntent(o: AiEditObjectReference): AiEditIntent {
  const t = objIntentText(o);
  if (o.imageDataUrl) {
    // 参照画像あり: メモが「素材語」または「面の仕上げ（壁面緑化・タイル・塗装・造作 等）」なら
    // 面へのテクスチャ/仕上げ適用（material＝形を保ち面全体へ適用）。それ以外は家具の差し替え（replace）。
    // 壁/床/天井の仕上げを「家具の差し替え」として扱うと面の形が崩れる・面全体に反映されない（③④・260717）。
    return RE_MATERIAL.test(t) || RE_SURFACE.test(t) ? 'material' : 'replace';
  }
  // 参照画像なし（テキストのみ）: 削除 > 照明/環境 > 仕上げ/色/面 > 一般テキスト の優先順で判定。
  if (RE_REMOVE.test(t)) return 'remove';
  if (RE_LIGHTING.test(t)) return 'lighting';
  if (RE_FINISH.test(t) || RE_MATERIAL.test(t) || RE_SURFACE.test(t)) return 'finish';
  return 'text';
}

/** NormalizedRect の外接矩形（多角形は頂点 min/max、矩形は x/y/width/height）を 0..1 で返す。 */
function bboxOf(r: NormalizedRect): { x: number; y: number; w: number; h: number } {
  if (r.points && r.points.length >= 3) {
    let minX = Infinity;
    let minY = Infinity;
    let maxX = -Infinity;
    let maxY = -Infinity;
    for (const p of r.points) {
      minX = Math.min(minX, p.x);
      minY = Math.min(minY, p.y);
      maxX = Math.max(maxX, p.x);
      maxY = Math.max(maxY, p.y);
    }
    if ([minX, minY, maxX, maxY].every((v) => Number.isFinite(v))) {
      return { x: minX, y: minY, w: Math.max(0, maxX - minX), h: Math.max(0, maxY - minY) };
    }
  }
  return { x: r.x, y: r.y, w: r.width, h: r.height };
}

/**
 * 領域の座標テキスト（画像編集・キャプション生成で共通）。
 * 多角形マスクでも「矩形（左/上/幅/高さ）」で Gemini に伝える（260702 クライアント報告「多角形は精度が低い」対応）。
 * 画像モデルは頂点列より単純な矩形の方が遥かに正確に領域を把握できるため、矩形＝矩形指定と同等の精度になる。
 * 実際の多角形の形は表示側 compositeMaskedEdit で厳密にクリップするので、テキストが矩形でも最終出力は損失なし。
 * 「境界線を描かない」制約は buildAiEditConstitution 内に一度だけ置き、ここでは繰り返さない（多重指示で逆に線を誘発するのを防ぐ）。
 */
function formatPlacement(r: NormalizedRect): string {
  const b = bboxOf(r);
  return `左${(b.x * 100).toFixed(1)}%, 上${(b.y * 100).toFixed(1)}%, 幅${(b.w * 100).toFixed(1)}%, 高さ${(b.h * 100).toFixed(1)}%（画像全体に対する正規化座標）`;
}

/** ユーザー自由入力（領域メモ）をプロンプトへ載せる前のサニタイズ（制御文字除去・空白圧縮・長さ上限）。 */
function sanitizePromptText(s: string, max = 120): string {
  return Array.from(s)
    .map((ch) => (ch.charCodeAt(0) < 32 || ch.charCodeAt(0) === 127 ? ' ' : ch))
    .join('')
    .replace(/ +/g, ' ')
    .trim()
    .slice(0, max);
}

/**
 * オブジェクト1件のフォーカス領域テキスト（画像編集・キャプション生成で共通）。
 * 座標は「切り取り枠」ではなく「どこに注目して編集するか」の目印として提示する（260707 クライアント要望）。
 * 各領域のメモ（placementMemos・任意）があれば、その領域固有の指示として併記する。
 */
export function describeObjectPlacements(o: AiEditObjectReference): string {
  if (o.placements.length === 0) {
    return '配置矩形未指定（画像内の適切な位置に自然に配置）';
  }
  const memoFor = (i: number) => {
    const m = o.placementMemos?.[i]?.trim();
    return m ? `／この領域の指示: ${sanitizePromptText(m)}` : '';
  };
  if (o.placements.length === 1) {
    return `フォーカス領域（この範囲に写る対象に注目して編集）: ${formatPlacement(o.placements[0])}${memoFor(0)}`;
  }
  return o.placements.map((p, j) => `フォーカス領域${j + 1}: ${formatPlacement(p)}${memoFor(j)}`).join(' / ');
}

